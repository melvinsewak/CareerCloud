        public IList<SecurityLoginsRolePoco> GetAll(params Expression<Func<SecurityLoginsRolePoco, object>>[] navigationProperties)
        {
            var result = new List<SecurityLoginsRolePoco>();

            using (SqlConnection sqlConnection = new SqlConnection(SqlUtility.ConnectionString))
            {
                SqlCommand sqlCommand = new SqlCommand();
                sqlCommand.CommandText = @"SELECT [Id]
                                              ,[Login]
                                              ,[Role]
                                              ,[Time_Stamp]
                                          FROM [dbo].[Security_Logins_Roles]";
                sqlCommand.Connection = sqlConnection;

                sqlConnection.Open();
                SqlDataReader sqlDataReader = sqlCommand.ExecuteReader();

                while (sqlDataReader.Read())
                {
                    var poco = new SecurityLoginsRolePoco();
                    poco.Id = (Guid)sqlDataReader[0];
                    poco.Login = (Guid)sqlDataReader[1];
                    poco.Role = (Guid)sqlDataReader[2];
                    poco.TimeStamp = (sqlDataReader[3].GetType()).Equals(typeof(System.DBNull)) ? null : (byte[])sqlDataReader[3];

                    result.Add(poco);
                }
                sqlConnection.Close();
            }
            return result;
        }

